export function getSurahByPage(page) {
  if (page === 0) return null
  let current = surahPageMap[0]
  for (const surah of surahPageMap) {
    if (surah[0] <= page) current = surah
    else break
  }
  return {
    number: current[1],
    name: current[2],
    arabic: current[3],
    ayahs: current[4],
    startPage: current[0]
  }
}